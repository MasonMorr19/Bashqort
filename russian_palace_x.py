def get_all_vocab() -> List[Tuple[str, str, str]]:
    """Get all vocabulary as (russian, english, category) tuples."""
    all_words = []
    for cat, words in VOCAB.items():
        for rus, eng in words:
            all_words.append((rus, eng, cat))
    return all_words

def toggle_word(russian: str, english: str):
    """Toggle a word in the selected list."""
    word = (russian, english)
    if word in st.session_state.selected_words:
        st.session_state.selected_words.remove(word)
    else:
        st.session_state.selected_words.append(word)

def get_word_count() -> int:
    """Get total word count."""
    return sum(len(words) for words in VOCAB.values())

