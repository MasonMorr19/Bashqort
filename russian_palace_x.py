@st.cache_data
def generate_audio(text: str, lang: str = 'ru') -> bytes:
    """Generate audio for Russian text using gTTS."""
    try:
        tts = gTTS(text=text, lang=lang, slow=False)
        audio_bytes = io.BytesIO()
        tts.write_to_fp(audio_bytes)
        audio_bytes.seek(0)
        return audio_bytes.read()
    except Exception as e:
        st.error(f"Audio generation failed: {e}")
        return None

def audio_player(text: str, key: str):
    """Create an audio player button for the given Russian text."""
    if st.button("üîä", key=f"audio_{key}", help=f"Listen to: {text}"):
        audio_data = generate_audio(text)
        if audio_data:
            st.audio(audio_data, format='audio/mp3')

